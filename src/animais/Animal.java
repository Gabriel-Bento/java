package animais;

public class Animal {
	
	// variáveis = atributos (substantivos)
	String nome;
	String especie;
	
	// métodos = comportamentos (verbos)
	public void locomover() {
		System.out.println("Animal locomovendo-se...");		
	}
	
	public void comer() {
		System.out.println("Animal comendo...");
	}
	
	public void dormir() {
		System.out.println("Animal dormindo...");
	}

	

}
